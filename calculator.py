print("🧮Bem vindo ao calculador de numeros inteiros🧮")
primeiro_valor = int(input("🥇Digite o primeiro valor: "))
segundo_valor = int(input("🥈Digite o segundo valor: "))

soma = primeiro_valor + segundo_valor
subtracao = primeiro_valor - segundo_valor
divisao = primeiro_valor / segundo_valor
multiplicacao = primeiro_valor * segundo_valor
print ("➕O resultado da sua soma foi {}".format(soma))
print(f"➖o resultado da subtracao foi: {subtracao}")
print(f"➗o resultado da divisao foi: {divisao:.2f}")
print(f"✖️o resultado da multiplicac1ao foi: {multiplicacao}")